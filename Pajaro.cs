using System;

class Pajaro{
    private string nombre;
    private string especie;
    private string poder;
    private int energia;

    static void CrearPajaro(string newNombre, string newEspecie, string newPoder, int newEnergia){
        nombre = newNombre;
        especie = newEspecie;
        poder = newPoder;
        energia = newEnergia;
    }
}